data = [3, 45, 6, 8, 324, 56, 23, 45, 98, 777, 678, 83, 65, 98]
print('Lista elemeinek száma: ', len(data))
print('Lista első eleme: ', data[0])
print('Lista utolsó eleme: ', data[-1])
print('A lista 3. és 5. tag összege: ', data[2] + data[4])
print('A lista legkisebb tagja: ', min(data))
print('A lista legnagyobb tagja: ', max(data))
print('A lista elemeinek összege: ', sum(data))
print('A lista elemeinek átlaga: ', sum(data) / len(data))
